# You create a new folder using the command:

# mkdir my\ folder

# alternately you could write: 

# mkdir 'my folder'

# You then enter it by using the command for changing directory (cd) and the name of the folder you want to navigate to:

# cd my\ folder

# To create a new file you use the touchcommand when you are in the place where you want it to be located:

# touch incrementor.py

# You can use the command for listing folder contents, ls, to check that it is created and open it in Mu using the Load button.